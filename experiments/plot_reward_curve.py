import argparse
from pathlib import Path
from typing import Optional

import matplotlib.pyplot as plt
import pandas as pd


def rolling_mean(s: pd.Series, window: int) -> pd.Series:
    if window <= 1:
        return s
    return s.rolling(window=window, min_periods=max(1, window // 3)).mean()


def resolve_monitor_csv(monitor_csv: Optional[str], monitor_dir: Optional[str]) -> Path:
    if monitor_csv:
        p = Path(monitor_csv)
        if p.exists():
            return p
        raise FileNotFoundError(f"Monitor CSV not found: {p}")

    if not monitor_dir:
        raise ValueError("Provide either --monitor-csv or --monitor-dir")

    d = Path(monitor_dir)
    if not d.exists() or not d.is_dir():
        raise FileNotFoundError(f"Monitor dir not found: {d}")

    candidates = sorted(d.glob("monitor_*.csv"), key=lambda x: x.stat().st_mtime, reverse=True)
    if not candidates:
        raise FileNotFoundError(f"No monitor_*.csv files found in: {d}")
    return candidates[0]


def main() -> None:
    prs = argparse.ArgumentParser(
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
        description="Plot SB3 Monitor reward curve (Episode vs Reward).",
    )
    prs.add_argument(
        "--monitor-csv",
        type=str,
        default="",
        help="Path to SB3 Monitor CSV (generated by Monitor wrapper).",
    )
    prs.add_argument(
        "--monitor-dir",
        type=str,
        default="",
        help="Directory containing monitor_*.csv. If set, uses the latest file.",
    )
    prs.add_argument("--title", type=str, default="Training Convergence of PPO Agent", help="Plot title.")
    prs.add_argument("--smooth", type=int, default=20, help="Rolling mean window (episodes).")
    prs.add_argument("--out", type=str, default="outputs/reward_curve.png", help="Output image path.")
    args = prs.parse_args()

    monitor_csv = resolve_monitor_csv(args.monitor_csv or None, args.monitor_dir or None)

    # SB3 monitor: first line is a JSON header starting with '#'
    df = pd.read_csv(monitor_csv, comment="#")
    if "r" not in df.columns:
        raise ValueError(f"Expected column 'r' in monitor CSV. Got: {df.columns.tolist()}")

    df["episode"] = range(1, len(df) + 1)
    df["r_smooth"] = rolling_mean(df["r"], args.smooth)

    out_path = Path(args.out)
    out_path.parent.mkdir(parents=True, exist_ok=True)

    plt.figure(figsize=(7.5, 4.5))
    plt.plot(df["episode"], df["r"], alpha=0.35, label="Episode reward")
    plt.plot(df["episode"], df["r_smooth"], linewidth=2.0, label=f"Rolling mean (window={args.smooth})")
    plt.title(args.title)
    plt.xlabel("Episode")
    plt.ylabel("Reward")
    plt.grid(True, alpha=0.3)
    plt.legend()
    plt.tight_layout()
    plt.savefig(out_path, dpi=200)
    print(f"Wrote: {out_path}")
    print(f"Used monitor: {monitor_csv}")


if __name__ == "__main__":
    main()

